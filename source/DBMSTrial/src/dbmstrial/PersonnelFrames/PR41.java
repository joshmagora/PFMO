    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        try{
        conn = ConnectTESTING.Connect();
        String str = "select emp_no as 'Employee Number', concat(emp_fname,' ',emp_lname)"
                + " as 'Name', job_desc as 'Job Description' from pfmo.workerInfo"
                + " where emp_no = '"+empNum+"'";
        ps = conn.prepareStatement(str);
        rs = ps.executeQuery();
        if(rs.next()){
            accEmpNo.setText(rs.getString("Employee Number"));
            accEmpName.setText(rs.getString("Name"));
            accJob.setText(rs.getString("Job Description"));
        }
        String str1 = "select sr_no as 'SR Number', section_desc"
                + " as 'Section', status_desc as 'Status' from pfmo.accrec1"
                + " where emp_no = '"+empNum+"'";
        ps = conn.prepareStatement(str1);
        rs = ps.executeQuery();
        accList.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        } 
    }//GEN-LAST:event_formWindowActivated

