    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        try{
        conn = ConnectTESTING.Connect();
        String str = "select * from pfmo.sr_info1"
                + " where sr_no = '"+srNum+"'; ";
        ps = conn.prepareStatement(str);
        rs = ps.executeQuery();
        if(rs.next()){
            txt_srNumber.setText(rs.getString("sr_no"));
            txt_srName.setText(rs.getString("Requestor"));
            txt_srDesignation.setText(rs.getString("position_desc"));
            txt_srContact.setText(rs.getString("contact_no"));
            txt_srDate.setText(rs.getString("sr_date"));
            txt_peType.setText(rs.getString("pe_type"));
            txt_srDept.setText(rs.getString("deptofc_code"));
            txt_srNature.setText(rs.getString("nature_request"));
            lbl_peDesc.setText(rs.getString("pe_desc"));
            lbl_peUnitNum.setText(rs.getString("pe_unit_no"));
            
        }
        
        String str2 = "select status_desc from pfmo.status";
        ps = conn.prepareStatement(str2);
        rs = ps.executeQuery();
        while(rs.next()){
            cmb_RecStatus.addItem(rs.getString("status_desc"));
        }
        String str3 = "select section_desc from pfmo.section";
        ps = conn.prepareStatement(str3);
        rs = ps.executeQuery();
        while(rs.next()){
            cmb_Section.addItem(rs.getString("section_desc"));
        }
        String str4 = "select rt_desc from pfmo.request_type";
        ps = conn.prepareStatement(str4);
        rs = ps.executeQuery();
        while(rs.next()){
            cmb_ReqType.addItem(rs.getString("rt_desc"));
        }
        String str5 = "select rec_code from pfmo.recomm";
        ps = conn.prepareStatement(str5);
        rs = ps.executeQuery();
        while(rs.next()){
            cmb_Rec.addItem(rs.getString("rec_code"));
        }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        } 
    }//GEN-LAST:event_formWindowActivated

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        String sec="",emp="",rt="",stat="";
        String date = datePicker1.getDate().toString();
        int response = JOptionPane.showConfirmDialog(this,"Do you want to send this request?",
                  "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if (response == JOptionPane.YES_OPTION){
        try{
            conn = ConnectTESTING.Connect();
            String section = " select section_code from pfmo.section"
                    + " where section_desc = '"+cmb_Section.getSelectedItem()+"'; ";
             ps = conn.prepareStatement(section);
             rs = ps.executeQuery();
             if (rs.next()) sec = rs.getString("section_code");
            String employee = " select emp_no from pfmo.employee"
                    + " where section_code = '"+sec+"' and"
                    + " position_code = 'S'; ";
             ps = conn.prepareStatement(employee);
             rs = ps.executeQuery();
            if (rs.next()) emp = rs.getString("emp_no");
            String requestType = " select rt_code from pfmo.request_type"
                    + " where rt_desc = '"+cmb_ReqType.getSelectedItem()+"'; ";
             ps = conn.prepareStatement(requestType);
             rs = ps.executeQuery();
            if (rs.next()) rt = rs.getString("rt_code");
            String status = " select status_code from pfmo.status"
                    + " where status_desc = '"+cmb_RecStatus.getSelectedItem()+"'; ";
             ps = conn.prepareStatement(status);
             rs = ps.executeQuery();
             if (rs.next()) stat = rs.getString("status_code");
            String str = " update pfmo.service_request"
                    + " set status_code = '"+stat+"', "
                    + " section_code = '"+sec+"', "
                    + " rt_code = '"+rt+"', "
                    + " rec_code = '"+cmb_Rec.getSelectedItem()+"', "
                    + " target_date= '"+date+"', "
                    + " emp_no = '"+emp+"', "
                    + " active = '1' "
                    + " where sr_no = '"+srNum+"'; ";
                ps = conn.prepareStatement(str);
                ps.execute();
                JOptionPane.showMessageDialog(null,"Submitting request was successful");
                this.dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        } 
        } else JOptionPane.showMessageDialog(null, "Submitting request cancelled");
    }//GEN-LAST:event_jButton1MouseClicked

