    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        try{
        conn = ConnectTESTING.Connect();
        String str = "select borrow_id as 'Borrow ID',Type,Description,Quantity from pfmo.borrow_items "
                + "where bslip_no = '"+slipNum+"' ; ";
        ps = conn.prepareStatement(str);
        rs = ps.executeQuery();
        tbl_bslip.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        } 
    }//GEN-LAST:event_formWindowActivated

    private void tbl_bslipMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_bslipMouseClicked
        sk162 = new SK162();
        int index = tbl_bslip.getSelectedRow();
        TableModel model = tbl_bslip.getModel();
       
        sk162.borrow = model.getValueAt(index, 0).toString();
        sk162.type = model.getValueAt(index, 1).toString();
        sk162.desc = model.getValueAt(index, 2).toString();
        sk162.quantity = model.getValueAt(index, 3).toString();
        sk162.setVisible(true);
        tbl_bslip.repaint();
    }//GEN-LAST:event_tbl_bslipMouseClicked

    private void btn_doneMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_doneMouseClicked
        try{
            String str = "update pfmo.bslip"
                    + " set status_code = 'O'"
                    + " where bslip_no = '"+slipNum+"' ;";
            ps = conn.prepareStatement(str);
            ps.execute();
            this.dispose();
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btn_doneMouseClicked

